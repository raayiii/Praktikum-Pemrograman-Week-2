#include <iostream>
using namespace std;

int main() {
	int x = 5, y = 10;
	cout << "x == y : " << (x == y) << endl;
	cout << "x != y : " << (x != y) << endl;
	cout << "x > y : " << (x > y) << endl;
	cout << "x < y : " << (x < y) << endl;
	cout << "x >= y : " << (x >= y) << endl;
	cout << "x <= y : " << (x <= y) << endl;
return 0;
}
