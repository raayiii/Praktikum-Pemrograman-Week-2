#include <iostream>
using namespace std;

int main() {
	int a = 10, b=3;
	cout << "Penjumlahan: a + b = " << (a + b) << endl;
	cout << "Pengurangan: a - b = " << (a - b) << endl;
	cout << "Perkalian: a * b = " << (a * b) << endl;
	cout << "Pembagian: a / b = " << (a / b) << endl;
	cout << "Modulus: a % b = " << (a % b) << endl;
	return 0;
}
